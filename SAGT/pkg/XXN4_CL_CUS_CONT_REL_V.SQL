 
  CREATE OR REPLACE VIEW XXN4_CL_CUS_CONT_REL_V
  AS 
  SELECT 
    ID,
    CL_PARTY_ID PARTY_ID,
    'ORGANIZATION' PARTY_TYPE,
    CL_CONTACT_PARTY_ID SUBJECT_ID,
    'PERSON' SUBJECT_TYPE,
    'HZ_PARTIES' SUBJECT_TABLE_NAME,
    CL_PARTY_ID OBJECT_ID,
    'ORGANIZATION' OBJECT_TYPE,
    'HZ_PARTIES' OBJECT_TABLE_NAME,
    'CONTACT_OF' RELATIONSHIP_CODE,
    'CONTACT' RELATIONSHIP_TYPE,    
    'HZ_WS' CREATED_BY_MODULE,
    CL_RELAT_STATUS,
    CL_RELATIONSHIP_ID
FROM XXN4_SHIPPING_LINES
WHERE 1 = 1
      and CL_PARTY_ID is not null and CL_RELATIONSHIP_ID is null
      and ID = 17;

select *
from XXN4_CL_CUS_CONT_REL_V;
